<h2 class="" >&nbsp <b style="color: black;">INFES</b></h2>
<p class="flow-text">&nbsp&nbsp&nbsp&nbspO Estar atento a segurança alimentar e educar futuros profissionais e cidadãos para estímulo e realização de atividades agroprodutivas foram um impulso é a criaçao do projeto <i>Núcleo de Unidades Pedagógicas Integradoras Agroprodutivas</i>, o Nupia. Uma iniciativa do Instituto Federal do Rio Grande do Sul (IFRS), Campus Bento Gonçalves, em parceria com a Empresa Brasileira de Pesquisa Agropecuária (Embrapa), contando com o apoio da Fundação Estadual de Pesquisa Agropecuária (Fepagro).</p>

<p class="flow-text">&nbsp&nbsp&nbsp&nbspO primeiro objetivo do Nupia a ser alcançado é a integração do ensino com a pesquisa e extensão tendo como base os sistemas agroprodutivos locais. Para isso, pretende-se instalar em diferentes locais do Estado vitrines agroprodutivas que atendam as culturas mais adaptadas áquele território e que possam ser fonte de desenvolvimento produtivo local. "Estamos promovendo encontros de aproximação entre as instituições parceiras para identificarmos o potencial de colaboração", falou o pesquisador Alexandre Hoffmann, da Embrapa Uva e Vinho, Unidade de pesquisas localizada também em Bento Gonçalves.</p>
